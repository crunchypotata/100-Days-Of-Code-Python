data = int(input("Enter year when you were born: "))
if data >= 1925 and data <= 1946:
  print("You are a Traditionalist")
elif data >= 1947 and data <= 1964:
  print("You are a Baby Boomers")
elif data >= 1965 and data <= 1981:
  print("You a Generation X person")
elif data >= 1982 and data <= 1995:
  print("You are a Millenials")
elif data >= 1996 and data <= 2015:
  print("You are Generation Z")
